public class Ex4ChangeToFahrenheit {
	public static void main(String[] args) {
// An application that converts 37 degrees Celsius to Fahrenheit.
		int F = 37; // Celsius
		double a = 1.8; 
		int b = 32;
		double C; // Fahrenheit
		
		C = (F-b)/a;
		
		System.out.println(F+" degrees Fahrenheit equal "+ C+ " degrees Celsius.");
	}
}
